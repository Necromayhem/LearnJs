// console.log(findMax([-1, -2, -3, -4, -5])); // Ожидаемый результат: -1


function findMax(array) {
    if(array.length == 1) return array[0]

    let maxOfRest = findMax(array.slice(1))

    if(array[0] > maxOfRest){
        return array[0]
    } else{
        return maxOfRest
    }

}


