// 1) Задача: Нахождение максимального элемента в массиве
// Напиши рекурсивную функцию, которая находит максимальное значение в массиве чисел. Назовем эту функцию findMax.
// Подсказка:
// Технически базовый случай - когда массив состоит из одного элемента, функция должна вернуть этот элемент.
// В рекурсивном случае разбивай массив на две части: первый элемент и оставшиеся элементы. Найди максимум из этих двух частей.

// Пример:
// function findMax(array) {
//     // Твоя рекурсивная функция
// }

// console.log(findMax([1, 2, 3, 4, 5])); // Ожидаемый результат: 5
